def set_cell(table, r, c, text):
    """清空单元格并填入新文本（保留格式结构）"""
    cell = table.cell(r, c)
    # 清空所有段落的 runs
    for para in cell.paragraphs:
        for run in para.runs:
            run.text = ''
    # 在第一个段落写入
    p = cell.paragraphs[0]
    if p.runs:
        p.runs[0].text = text
    else:
        p.add_run(text)


